#include <iostream>

using namespace std;

int main() {

    int a = 10;

    //preinkrementacja
    cout << a << " " << ++a << endl;

    a = 10;
    //postinkrementacja
    cout << a << " " << a++ << endl;
    cout << a << endl;

    return 0;

}
